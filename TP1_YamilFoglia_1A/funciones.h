#include <stdio.h>
#include <stdlib.h>


/** \brief Suma de dos numeros
 *
 * \param Primer variable a sumar
 * \param Segunda variable a sumar
 * \return Resultado de la suma
 *
 */
float suma(float numeroUno, float numeroDos);


/** \brief Resta de dos numeros
 *
 * \param Primer variable a restar
 * \param Segunda variable a restar
 * \return Resultado de la resta
 *
 */
float resta(float numeroUno, float numeroDos);


/** \brief Division de dos numeros
 *
 * \param Variable del dividendo
 * \param Variable del divisor
 * \return Resultado de la division
 *
 */
float division(float numeroUno, float numeroDos);


/** \brief Multiplicacion de dos numeros
 *
 * \param Variable del multiplicando
 * \param Variable del multiplicador
 * \return Resultado de la multiplicacion
 *
 */
float multiplicacion(float numeroUno, float numeroDos);


/** \brief Factorial de un numero
 *
 * \param Variable para sacar el factorial
 * \return Resultado del factorial
 *
 */
float factorial(float numero);

int validarDivision(float numeroDos);

int validarNumero(float numero);

int validarFactorial(float numero);
